package main.java.backend.model;

public class ContactDetails {

    private String contactDetailsName;
    private String contactDetailsEmail;
    private String contactDetailsMobile;

    public String getContactDetailsName() {
        return contactDetailsName;
    }

    public void setContactDetailsName(String contactDetailsName) {
        this.contactDetailsName = contactDetailsName;
    }

    public String getContactDetailsEmail() {
        return contactDetailsEmail;
    }

    public void setContactDetailsEmail(String contactDetailsEmail) {
        this.contactDetailsEmail = contactDetailsEmail;
    }

    public String getContactDetailsMobile() {
        return contactDetailsMobile;
    }

    public void setContactDetailsMobile(String contactDetailsMobile) {
        this.contactDetailsMobile = contactDetailsMobile;
    }

}
